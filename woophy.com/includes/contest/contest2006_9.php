<h4>September 2006 - Food and Drink</h4>
<p><strong>From hundreds of tasty photos we choose the 'three noodle eaters from Beijng' by Ewest as winner. It looks like three workers enjoying a quick lunch. From my own experience in China I know that even a simple lunch can be very tasteful (or horrible if you order the wrong thing like chicken feet). Anyway a very colorful and dynamic photo.</strong></p>
<p>Congratulations Ewest thanks for your beautiful photos from China. You won a copy of 'Hungry Planet' by Peter Menzel. The honorable mentions go to Paul Leger for his oktopus in Greece, cooks in Beijing by Sercas72, and the heaps of Pak Soi (that is what we call it) in again Beijing by Patt. Thanks all of your for the mouth watering photos.</p>
