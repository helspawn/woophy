<h4>February 2006 - Waste, pollution and environment</h4>
<p><strong>Woophy presents the winner of our February competition 'Waste, pollution and environment' Rikus with his photo of the left oil barrels in Palmer Station Antarctica. Rikus congratulations, you won the book 'Antarctica' by Mike Lucas.</strong></p>
<p>The honourable mentions go to Pistachon from Mexico and Antonio from Estonia. Pistachon is a true master of waste and pollution photo's. Antonio managed to capture the dirtiest beach on earth with more plastic bottles on it than in a supermarket. The both of you congratulations.</p>
