<p>Sara G. and Vahepoto congratulations with your first prize. You both have won the book Witness in Our Time: Working Lives of Documentary Photographers by Ken Light.<br/>
The honourable mentions are for laura lizancos mora, Bogers and Couthino. Also congratulations.</p>
