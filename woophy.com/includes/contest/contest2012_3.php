<h2>Results March 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/1258200" target="_blank"><img src="<?php echo ROOT_PATH?>images/contest/2012_1258200.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/ZoiKoraki" target="_blank">ZoiKoraki</a></p>

<p><strong>This month it was San Francisco’s turn to once again choose a winner. However this time it also meant me going it alone on judging duties because Terry has been up to his ears working on new and wonderful things for us. And just what are those new and wonderful things you ask?? Well, lets just say they have to do with iPhones, iPads, and our world famous Woophy map.</strong></p>

<p>But even though I went it alone on judging this month, and even though the Animals category brought in over 900 entries, you can rest assured that I still reviewed each and every submission…and to be honest it was actually quite fun. However picking a “winner” among so many great, interesting, meaningful, and heart-warming photos was truly a challenge.</p>

<p>So how did I end up doing it??</p>
<p>Well, if I would have just judged on cuteness alone I would have never been able to decide. March’s contest was cuteness overload…so many sleeping cats and cute baby animals!! If I had gone the cuteness route there would’ve simply been no way to declare a winner.<br>
</p>
<p>Luckily though, as I went through the submissions I started to sense that there were indeed some things I was looking for. For one, I found I liked it when the animals were as free as possible. This meant that even though humans and animals can have very special and meaningful relationships, I kept finding myself drawn to photos where animals were free of human bindings or confinement. Also, if animals were seen as a part of the larger world, I really felt drawn to those too.<br/>
So those two things really helped me narrow the field a bit. But oddly enough they didn’t really come into play as far as how I picked the winner.<br>
</p>
<p>The winning photo by <b><a href="<?php echo ROOT_PATH?>member/ZoiKoraki" target="_blank" rel="nofollow">ZoiKoraki</a></b> was chosen purely because of how it made me feel every time I saw it. It’s sassy, funny, and playful. It just felt right to declare it the winner because it really seemed to exude that “Woophy” spirit.</p>

<p>But the honorable mentions for last month were no slouches either.  The second place photo (if I could give a 2nd place prize) would definitely be<b> <a href="<?php echo ROOT_PATH?>member/Christian%20C" target="_blank" rel="nofollow">Christian C</a></b>’s Swiss cows overlooking a mountain range. The reason for this choice is because that photo really represents what I was looking for as I described above. It shows animals in a fairly free state as well as being a part of a larger world.<br/>
And as for the rest of the honorable mentions, well, these were all just superb photos I really felt everyone should see. <b><a href="<?php echo ROOT_PATH?>member/Jan%20Hemels" target="_blank" rel="nofollow">Jan</a></b>’s snake shot is really amazing. And what about those stick insects?! I just had to make sure you guys saw these photos if you hadn’t already!</p>

<p>So anyway, that’s if for me/us here in San Francisco. Next month it’s Rotterdam’s turn to judge “Landscapes” and then we’ll be back in May to judge the “People” category!!<br>
Thanks everyone :-)<br>
<br>
Josh</p>
<p>P.S. In addition to finding the winner Animal photo, I’m also pretty sure I have found the most terrifying photo on Woophy. To me it’s ZoiKoraki’s cave owl thingy. If I had come across that in real life there is no way I would have stopped to take a picture. I would have dropped my camera and ran! Creepy!!</p>

