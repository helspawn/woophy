<h2>Results June 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/1323976" target="_blank"><img src="<?php echo ROOT_PATH?>images/contest/2012_1323976.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/s%C3%B8ren%20skov" target="_blank">Søren Skov</a></p>

<p><strong>Even though quantity was lower the quality of entries remained high. Like every month we first judged all photos individually and then looked if there were similarities in our favorites. And again like so often it turned out that we were quite unanimous in our judgment. Without having discussed it beforehand we all seemed to go for the black and white photos.</strong></p>

<p>And one photo in particular grabbed the theme ‘Work’ best in our opinion and that is the picture of the two firefighters by <a href="<?php echo ROOT_PATH?>member/s%C3%B8ren%20skov" target="_blank">Søren Skov</a>. One of them is still looking in control or even proud after having fought this obvious tough battle but the other one is clearly very tired and looks defeated. The smoke in the background indicates that the fight might not even be over yet.</p>

<p>For us this photo by <a href="<?php echo ROOT_PATH?>member/s%C3%B8ren%20skov" target="_blank">Søren Skov</a> showing all the blood sweat and tears of work is a clear winner. Congratulations Søren you won a nice photo book. Hope we will see more of your photography on Woophy.</p>

<p>The two honorable mentions go to Geert Geenen for the nice symmetry with the winning picture and to DraMill for his very beautiful picture of a Croatian cobbler. What a workshop this guy has.</p>

<p>Thank you <a href="<?php echo ROOT_PATH?>member/s%C3%B8ren%20skov" target="_blank">Søren Skov</a>, <a href="<?php echo ROOT_PATH?>member/geert%20geenen" target="_blank">Geert Geenen</a> and <a href="<?php echo ROOT_PATH?>member/DraMill" target="_blank">DraMill</a> for your impressive photography!</p>

