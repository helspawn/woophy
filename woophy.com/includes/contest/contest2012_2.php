<h2>The Honorable Mentions</h2>


<p>As written above it was very difficult to pick just one winner this time. E.g. the photos by <a href="<?php echo ROOT_PATH?>member/sawsengee" target="_blank">Sawsengee</a> are so breathtaking perfect that I used them as an image on my Woophy business card. And the photos of <a href="<?php echo ROOT_PATH?>member/geert+geenen" target="_blank">Geert Geenen</a> always make me laugh, he is such a great observer of the often surrealistic way people behave.</p>

