<h2>Results February 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/1270733" target="_blank"><img src="<?php echo ROOT_PATH?>images/contest/2012_1270733.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/Julia+Franke" target="_blank">Julia Franke</a></p>

<p><strong>Thank you all for yet another round of great photography in last month’s Cityscape contest. Very interesting to see all your different city photos.</strong></p>

<p>For the jury it was a difficult month. Not so much to agree on the shortlist but the choice of the winner really gave us a headache. At some moment we had three potential winners, the breathtaking night panorama’s of <a href="<?php echo ROOT_PATH?>member/sawsengee" target="_blank">Sawsengee</a>, the humoristic photos full of people by <a href="<?php echo ROOT_PATH?>member/geert+geenen" target="_blank">Geert Geenen</a> and the original viewpoints by <a href="<?php echo ROOT_PATH?>member/Julia+Franke" target="_blank">Julia Franke</a>.</p>

<p>In fact they were all three perfect in their own right but since we had to choose one winner we picked the London bus by Julia. It is a very original photo and point of view of an iconic piece of city which is already photographed a million times before.</p>

<p>Congratulations <a href="<?php echo ROOT_PATH?>member/Julia+Franke" target="_blank">Julia</a>, thanks for your very original dynamic views. You won the book Material World a global family portrait.</p>

<p>Post your photos for this month’s theme ‘Animals’ <a href="<?php echo ROOT_PATH?>contest/submit">here</a>.</p>

