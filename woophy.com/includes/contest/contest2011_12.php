<h2>Results December 2011</h2>
<p><a href="<?php echo ROOT_PATH?>photo/390907"><img src="<?php echo ROOT_PATH?>images/contest/2011_390907.jpg" /></a></p>
<p><strong>Woophy is proud to announce the winner of the December contest <a href="<?php echo ROOT_PATH?>member/Christian+C" target="_blank" rel="nofollow">Christian C</a>. From almost 2,000 entries we chose his photo of the &quot;Wasp Spider&quot; as the one most representing nature.
</strong></p>
<p>We liked his macro photo because there was a lot of life in it. Some macro photos are very aesthetic or can look almost &quot;frozen&quot; but this photo is in every perspective dynamic. Not only the spider itself balancing upside down between the flowers, but also the colorful composition of the photo, the lines of the web and the plants and the clouds are full of live.<br />
<br />
Also we liked the fact that it is a very special picture of a very common subject which is always more interesting than the other way around. <br />
<br />
After having made a long list of nine photos it was clear to us that we would bring <a href="<?php echo ROOT_PATH?>member/Christian+C" target="_blank" rel="nofollow">Christians C</a>'s photo to Mars if we had to explain nature on earth to a Martian. If you look closer there are many essential elements of nature in this composition.  I mean there is flora and fauna, we have reproduction (the flowers), death(the almost dried out grass), water, sky, oxygen, photo syntheses etc. <br />
<br />
I think for an abstract subject as &quot;nature&quot; it is impossible to find one photo that &quot;says it all&quot; but this one sure comes close.<br />
<br />
Congratulations <a href="<?php echo ROOT_PATH?>member/Christian+C" target="_blank" rel="nofollow">Christian</a> you won our first competition this year.</p>


