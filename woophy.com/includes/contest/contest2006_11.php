<h1>Daily Life on Earth - Results</h1>
<h4>November 2006 - Work</h4>
<p><strong>From over 880 photos we choose the Fisherman by Poul Madsen as the winner of our November Work competition. It is the worried but also firm expression in his face which tells us all about his hard work. The beautiful light and out of focus background add more drama to the photo.</strong></p>
<p>Congratulations Poul you won a copy of the book "Photojournalism The Professionals' Approach" by Kenneth Kobr&eacute;. The honorable mentions go to Bogers for his workers on the scaffolds and to Janette for her photo of the workman in the sulfur heap.</p>
