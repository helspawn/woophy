<h2>Results April 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/1274990" target="_blank"><img src="<?php echo ROOT_PATH?>images/contest/2012_1274990.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/Luís+Borges+Alves" target="_blank">Luís Borges Alves</a></p>

<p><strong>This time it was Woophy Rotterdam that had the difficult task of finding a winner. The task was especially complicated because ‘Landscape’ is a very Woophy like subject with many people submitting good pictures.</strong></p>

<p>Yet there were only 3 pictures which ended up on all of our shortlists and since two of the three pictures belonged to the same photographers it was clear who should be the winner of the contest. <br/>
The only thing left was to choose the winning photo from his two entries. One picture was a very atmospheric, mysterious <a href="https://www.google.nl/search?q=turner&amp;hl=en&amp;prmd=imvns&amp;source=lnms&amp;tbm=isch&amp;ei=2k2xT42VG8Kl8gPnq8S3CQ&amp;sa=X&amp;oi=mode_link&amp;ct=mode&amp;cd=2&amp;ved=0CBcQ_AUoAQ&amp;biw=1599&amp;bih=827" target="_blank" rel="nofollow">Turner</a>-esque photo of an island in the mist. The other was a wide mountain landscape in black and white with beautiful depth emphasized by the mist hanging between the hills.</p>

<p>It was only because the latter of the two suited the theme of the contest a little bit more that we chose this one as the winner.</p>
<p>Congratulations<a href="<?php echo ROOT_PATH?>member/Lu%C3%ADs+Borges+Alves" target="_blank" rel="nofollow"> Luis Borges Alves</a> you won the Book <b>Paris: Portrait of a City</b> by Jean-Claude Gautrand


