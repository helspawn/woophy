<h2>Results January 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/88227"><img src="<?php echo ROOT_PATH?>images/contest/2012_88227.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/zerega" target="_blank" rel="nofollow">zerega</a></p>

<p><strong>Well this time it was Woophy San Francisco’s turn to choose the winner for Woophy’s World Photo Contest. And as most of you already know, January’s theme was “Culture”. This contest had over 700 entries, and even though that number is down from December’s contest, judging the contest was still quite an undertaking. There were just SO MANY amazing photos.</strong></p>

<p>Now to give you some insight into how the judging process went, Terry and I were the judges this time and we decided to start off by independently finding our Top 10 personal choices. Then we would share our top picks with each other, discuss things a bit, and (hopefully) declare a winner.</p>

<p>Now for my part, I, <a href="<?php echo ROOT_PATH?>member/WoophySF(JP)" target="_blank" rel="nofollow">WoophySF(JP)</a>, started off by looking for photos that I felt best represented mankind vs. nature. I initially felt that a strong representation of mankind’s existence and accomplishments would also translate into a strong representation of culture. So, in the beginning, I was looking for things that might have moved me if I were travelling along a road and happened to come across whatever was being shown in the photo. This meant things like castles, monuments, cites, etc. soon became leading candidates for me.</p>

<p>But then after a while, I started seeing more and more photos with actual people in them. And this changed my mind a bit. I then started to feel that photos best representing culture (at least to me) were ones where people were actually enjoying, or otherwise being involved with their culture.</p> 

<p>Terry on the other hand seemed mostly impressed by structures. He also seemed impressed with boats and activity near water (4 of his Top 10 photos had something to do with water).</p>

<p>Now as far as how the winning photo was eventually decided, from my perspective, when I first saw the photo, I felt it was almost the perfect representation of what the term “culture” means to me and what in my mind makes culture is so interesting. It really stood out to me.</p>

<p>In that photo I felt both an immense amount of pride as well as wonder coming through. Both of those traits speak directly to me about the power of culture and that’s what really made this photo a winner for me. I especially like how the man (a host??) has his arm raised as if he is showing off the vast expanse of the area. Then at the same time you have a man (possibly his guest) politely taking it all in.</p>

<p>But that was just me. What about Terry??</p>

<p>Well, as you might imagine, Terry also liked the photo and I was greatly relieved when I found out that he had also placed it in his Top 10. This made an almost impossibly difficult job just that much easier for us and after discussing things a bit we both decided to finalize the winner as well as recognize 4 great honorable mentions.</p>

<p>And well, that’s pretty much it!!</p>

<p>Thanks to everyone who entered the contest and congratulations to the winner and our 4 great honorable mentions!!</p>

