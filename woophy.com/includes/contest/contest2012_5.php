<h2>Results May 2012</h2>
<p><a href="<?php echo ROOT_PATH?>photo/1130747" target="_blank"><img src="<?php echo ROOT_PATH?>images/contest/2012_1130747.jpg" /></a></p>

<p>The winning photo by <a href="<?php echo ROOT_PATH?>member/zoidberg" target="_blank">zoidberg</a></p>


<p><strong>Well it&rsquo;s that time again for San Francisco to step up and judge the Woophy&rsquo;s World Contest. May&rsquo;s theme was People , and boy, let me tell you, we got a lot of People.</strong></p>

<p>We received almost 1000 entries (975 to be exact), and yes, I went though each and every one. Terry had to bow out on judging duties again this month because he (along with everyone at Woophy) has been working full steam trying to get Woophy Version 3 out the door to you. I think you&rsquo;re really going to like Woophy V3. We&rsquo;re all pretty excited about it and we&rsquo;re still making slight changes to it even as I write this. If we release V3 and it flops, it won&rsquo;t be from lack of trying I can tell you that! And always know this: V3 is just the beginning!!</p>

<p>But anyway, back to the contest.</p>

<p>Now, unlike some of the contests I’ve judged in the past, this one was a little easier to pick a winner. Because after I’d seen hundreds and hundreds of entries, when I finally came across the winning photo, it just really popped. It jumped out at me, grabbed me by the shoulders, and said: &ldquo;Here I am! Pick me! Pick me!!&rdquo;</p>

<p>And I think a big part of why it stood out so much is because for some reason, with this contest, we had wave upon wave of entries that really didn’t show people at their best. We had many entries showing children (and others) in need this month, which is perfectly fine, but with so many of the same kind of photo, it kind of takes the power out of the subject.</p>

<p>But with the winning photo it was different. It stood out. There was joy in it. There was passion in it. There was the desire for me to be right there with the photographer when he took it. It&rsquo;s just a great photo and I think <a href="<?php echo ROOT_PATH?>member/zoidberg" target="_blank">zoidberg</a> did an amazing job capturing such a lively group of People. Congratulations.<br/>
And as for the Honorable Mentions, well there isn’t really much to say. These are both amazing photos as well and it would be a crime not to mention them. The looks captured on these faces are priceless and VERY worthy of being mentioned as stand out photos. Great job to both <a href="<?php echo ROOT_PATH?>member/zeeg" target="_blank" rel="nofollow">Zeeg</a> and <a href="<?php echo ROOT_PATH?>member/ESRA+SIRMAN" target="_blank" rel="nofollow">Esra Sirman</a> for these truly great photos.</p>

<p>And, well, that&rsquo;s pretty much it for me. Next month it&rsquo;s back to Rotterdam for judging the Work theme (which I&rsquo;m excited to see). And after that it&rsquo;ll be back to us for Science.</p>

<p>Thanks everyone!!</p>

<p>-Josh / WoophySF(JP)</p>


