<h1>Daily Life on Earth - Results</h1>
<h4>January 2006 - Home Sweet Home</h4>
<p><strong>Woophy proudly presents the winner of our January competition 'Home Sweet Home' Goran Stamenkovic with his photo of a proud 'chicken farmer' in front of his house in Lipovica Serbia. Congratulations you have won a copy of ‘The Photography Book' (Phaidon Press)</strong></p>
<p>The jury also choose two honourable mentions one from Serge Haddad from France for his photo of Havana Cuba and one for Daniel Gustafsson from Sweden for his photo of an apartment building in Swinoujscie Poland. Also congratulations to you.</p>
