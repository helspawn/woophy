<p>One of the many great photos by Goran Stamenkovic on Woophy. Goran pictures life in strong black and white photos. Although some of his photos are somewhat gloomy there often also quite humorous. The proud expression of the chicken farmer sends us also a message that despite maybe bad economic or politic situation there is still room for individual joy.</p>

