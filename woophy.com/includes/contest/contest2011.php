<p>The month December it is <strong>&quot;Nature&quot;</strong> month. <a href="<?php echo ROOT_PATH?>contest/submit">Submit your photos</a> that best represent &quot;Nature&quot;.</p>

<p>Every photo on Woophy -old and new- can take part in the competition. <a href="<?php echo ROOT_PATH?>contest/submit">Submit</a> the Woophy photo id # in the contest widget. The id # of your photo can be found in the photo pop-up or the photo page. It is also allowed to add photos by other Woophy members.</p>

